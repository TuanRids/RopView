////////////////////////////////////////////////////////////////////////////////////////////////////
/// @file IIdentifierCollection.h
///
/// Declares the IIdentifierCollection interface.
////////////////////////////////////////////////////////////////////////////////////////////////////
#pragma once
#include <memory>

namespace Olympus
{
  namespace Inspection
  {
  class IIdentifierCollection
  {
  public:
    virtual ~IIdentifierCollection() = default;

    ///////////////////////////////////////////////////////////////////////////////////////////////
    /// Gets the identifiers count
    ///
    /// @returns The count.
    ///////////////////////////////////////////////////////////////////////////////////////////////
    virtual std::size_t GetCount() const = 0;

    ///////////////////////////////////////////////////////////////////////////////////////////////
    /// Gets a identifier
    ///
    /// @param index The index of the requested identifier in this collection.
    ///
    /// @returns The identifier 1 based.
    ///////////////////////////////////////////////////////////////////////////////////////////////
    virtual std::size_t GetIdentifier(std::size_t index) const = 0;

    /////////////////////////////////////////////////////////////////////////////////////////////
    /// Adds Identifier
    ///
    /// @param Identifier 1 based to add.
    /////////////////////////////////////////////////////////////////////////////////////////////
    virtual void Add(std::size_t identifier) = 0;

    /////////////////////////////////////////////////////////////////////////////////////////////
    /// Remove an identifier in this collection by giving its index.
    ///
    /// The indexes of the elements after the removed element are all decremented of 1.
    /// 
    /// @param index The identifier of the IIdentifierCollection desired to be removed.
    /////////////////////////////////////////////////////////////////////////////////////////////
    virtual void Remove(std::size_t index) = 0;
  };

  using IIdentifierCollectionPtr = std::shared_ptr<IIdentifierCollection>;
  using IIdentifierCollectionConstPtr = std::shared_ptr<const IIdentifierCollection>;

  }
}
