///////////////////////////////////////////////////////////////////////////////////////////////////
/// @file Inspection\IFormation.h.
///
/// Declares the IFormation interface
///////////////////////////////////////////////////////////////////////////////////////////////////

#pragma once
#include <memory>

namespace Olympus { namespace Inspection {

  class IFormation;

  using IFormationPtr = std::shared_ptr<IFormation>;
  using IFormationConstPtr = std::shared_ptr<const IFormation>;

  class IFormation
  {
  public:
    /////////////////////////////////////////////////////////////////////////////////////////////
    /// Destructor
    /////////////////////////////////////////////////////////////////////////////////////////////
    virtual ~IFormation() = default;

    /////////////////////////////////////////////////////////////////////////////////////////////
    /// Gets the number of elements on the primary axis.
    ///
    /// NDE file domain path "dataGroups"[index] / "ultrasound" / "compoundPulseEcho" or "linearPulseEcho" or "sectorialPulseEcho" / "elementAperture"
    /// 
    /// @returns The number of element primary axis.
    /////////////////////////////////////////////////////////////////////////////////////////////
    virtual int GetNumberOfElementPrimaryAxis() const = 0;

    /////////////////////////////////////////////////////////////////////////////////////////////
    /// Sets a number of elements on the primary axis.
    ///
    /// @param numberOfElementPrimaryAxis Number of element primary axis.
    /////////////////////////////////////////////////////////////////////////////////////////////
    virtual void SetNumberOfElementPrimaryAxis(int numberOfElementPrimaryAxis) = 0;
  };
}}
