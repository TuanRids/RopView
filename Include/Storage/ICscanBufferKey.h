////////////////////////////////////////////////////////////////////////////////////////////////////
/// @file ICscanBufferKey.h.
///
/// ICscanBufferKey is used as a unique key to retrieve a buffer and his descriptor.
////////////////////////////////////////////////////////////////////////////////////////////////////

#pragma once
#include <memory>
#include <string>


namespace Olympus {
  namespace Ultrasound {
    namespace Data
    {
      class ICscanBufferKey
      {
      public:
        virtual ~ICscanBufferKey() = default;

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// Gets beamset name
        ///
        /// @returns  The beamset name.
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        virtual const std::wstring& GetBeamSetName() const = 0;

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// Gets the beam index  
        ///
        /// @returns  The beam index.
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        virtual int GetBeamIndex() const = 0;

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// Gets the gate name associated with this CScan
        ///
        /// @returns  The gate name.
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        virtual const std::wstring& GetGateName() const = 0;
      };

      using ICscanBufferKeyConstPtr = std::shared_ptr<const ICscanBufferKey>;
    }
  }
}
