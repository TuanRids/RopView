////////////////////////////////////////////////////////////////////////////////////////////////////
/// @file IOriginalFileSource.h
///
/// The original file source holds filename and datetime metadata information.
///
////////////////////////////////////////////////////////////////////////////////////////////////////

#pragma once
#include <string>
#include <memory>

namespace Olympus
{
  namespace FileManagement
  {
    class IOriginalFileSource
    {
    public:
      virtual ~IOriginalFileSource() = default;

      /////////////////////////////////////////////////////////////////////////////////////////////
      /// Gets filename
      ///
      /// @returns The filename name.
      /////////////////////////////////////////////////////////////////////////////////////////////
      virtual const std::wstring& GetFileName() const = 0;

      /////////////////////////////////////////////////////////////////////////////////////////////
      /// Gets date time
      ///
      /// @returns The date time.
      /////////////////////////////////////////////////////////////////////////////////////////////
      virtual const std::string& GetDateTime() const = 0;
    };

    using IOriginalFileSourcePtr = std::shared_ptr<IOriginalFileSource>;
  }
}
