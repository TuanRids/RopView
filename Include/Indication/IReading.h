#pragma once
#include <memory>
#include <string>

namespace Olympus
{
  namespace Indication
  {
    /// A reading.
    class IReading
    {
    public:
      virtual ~IReading() = default;

      /////////////////////////////////////////////////////////////////////////////////////////////
      /// Gets the name of the reading.
      ///
      /// @returns The name of the reading.
      /////////////////////////////////////////////////////////////////////////////////////////////
      virtual const std::wstring& GetName() const = 0;

      /////////////////////////////////////////////////////////////////////////////////////////////
      /// Gets the value of the reading.
      ///
      /// @returns The value of the reading.
      /////////////////////////////////////////////////////////////////////////////////////////////
      virtual double GetValue() const = 0;

      /////////////////////////////////////////////////////////////////////////////////////////////
      /// Sets a value for the reading.
      ///
      /// @param value The value of the reading.
      /////////////////////////////////////////////////////////////////////////////////////////////
      virtual void SetValue(double value) = 0;
    };

    using IReadingPtr = std::shared_ptr<IReading>;
    using IReadingConstPtr = std::shared_ptr<const IReading>;
  }
}
